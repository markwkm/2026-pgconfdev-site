    contact information.
  </li>
  <li>
    On the day, at 17:00 look for your group leader in the concourse by
    Registration.
  </li>
  <li>
    Walk together to your restaurant, have dinner, conversation and a great
    time.
  </li>
  <li>
    Pay for your meal <b>individually</b>. This is a <b>self-pay dinner</b>, so
    do not forget your credit card!
  </li>
